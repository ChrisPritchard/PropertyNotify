    [Fact]
    public void AllParamsWithNamespaces()
    {
        var source = @"
namespace TestNamespace;

public partial class TestClass
{
    [PropertyNotify.Notify(pass_old_value: true, pass_changed_name: true, method_name: ""CallThis"")]
    public partial string TestProp { get; set; }

    private void CallThis(string name) {}
}".Replace("\r\n", "\n");

        var target = @"// <auto-generated/>
namespace TestNamespace;

using System.Collections.Generic;

public partial class TestClass
{
    private string _testProp;

    public partial string TestProp
    {
        get => _testProp;
        set
        {
            if (!EqualityComparer<string>.Default.Equals(_testProp, value))
            {
                var old_value = _testProp;
                _testProp = value;
                CallThis(nameof(TestProp), old_value);
            }
        }
    }

}
".Replace("\r\n", "\n");

        Assert.Equal(target, Convert(source));
    }
